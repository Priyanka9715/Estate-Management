<h2>History</h2>
<p>Four Generations of the Mathias family have been directly involved in the cultivation and processing of coffee and pepper. Aramane Estate was acquired by S L Mathias (1868-1940), himself the son of a coffee planter, in 1927. His son E J Mathias (1913-1992) carried on the family tradition for five decades and developed the estate further, planting coffee, clonal pepper and progressively improving the social welfare of the workers.<br>
In 1980, Peter Mathias joined the organization and in 1989 assumed the role of Managing Partner. In the last 25 years, Peter Mathias and his loyal and hard working team have transformed Kelagur Estate into one of India’s premier Tea and Coffee plantations. Currently Aramane Mathias Coffee and Tea Plantations is spread over a thousand acres and the output is in excess of 7,50,000 Kgs of premium quality finished produce annually. As the major employer of over 500 employees, their credo has always been people first!<br>
The United Planters Association of Southern India (UPASI) – a 120 year old organization formed by the three Southern states of Karnataka, Kerala and Tamil Nadu elected Peter Mathias as President of UPASI for the year 2013-14.
