<h2 class="card-title">About Soormane Falls</h2>
<p class="card-text"> Also known as Abbugudige Falls, Soormane Waterfall is a 40 feet waterfall formed by an unknown stream. The waterfall is located in a private estate surrounded by dense forest. The waterfall forms a beautiful crystal clear pond below which is a good place for visitors to swim and play in the water. This is a perfect retreat from the busy city life as it is one of the least explored waterfalls in Chikmagalur. Do take extreme care in any water body as it might slip and with kids do take extra care.
</p>
<h2 class="card-title">The panoramic view from the Ballalarayana Durga fort:</h2>
<p class="card-text"> Situated atop a hill that is 1509 metres high, the fort overlooks the town of Sunkasaale. The fort has lost its glory and stands as a distant reminder of its beautiful past. Visitors can find just a few shattered walls of the fort that still exists there as a proof of the grand structure that had once stood there with pride. In its completely ruined and dilapidated condition the fort remains as an example of an ancient structure that has been destroyed due to the lack of proper preservation.
However, the beauty of the surrounding green luxurious forests with steep slopes has not dimmed over time. The ruined fort presents a breath-taking view of the surroundings. The engulfing greenery charms visitors at all times of the year.
The Ballalarayana Durga is ideal for watching amazing sunsets.  During the evening hour of the sunset the whole place comes alive with colours of different hues. Another attraction of the fort is the calm atmosphere it offers to the visitors. Devoid of any noise and in the presence of an ever-blowing gentle breeze the place is a serene spot to relax and enjoy the beauty of nature.
</p>

<h2 class="card-title">Trekking to the Ballalarayana Durga fort:</h2>
<p class="card-text"> Apart from its historical significance the Ballalarayana Durga fort is also famous as a trekking destination. The most frequented trek route is the one that starts at Mundaje and takes one to the Bandeje Arbi before proceeding to the ruined fort. The trek to the fort is a difficult one and presents quite a few challenges to the trekker. It is easier for an experienced trekker to cover this route than for someone who is a novice in trekking.
</p>

<h2 class="card-title">What to carry on a trip to the Ballalarayana Durga fort:</h2>
<p class="card-text">Ballalarayana Durga is an isolated and ruined fort that stands atop a hilltop. As such it has no shops nearby. It is advisable to carry food while visiting the fort. Though the place is not preferred for setting up tents and passing the night there, Bandeje Arbi is a popular choice for camping. Hence, tents must also be carried if one intends to spend the night in one of the two places.
The nearest point where decent food can be bought is at Mundaje. The nearest petrol pump stands at Ujire, more than 8 km from the fort. Ujire is also the place where the nearest hospital can be found.
</p>

<h2 class="card-title">Best time to visit the Ballalarayana Durga fort:</h2>
<p class="card-text">The best time to visit the fort is from the middle of November to May. It is better to avoid visiting the fort from June to October as it is the rainy season. During this season the place is covered with lots of leeches, the rocks become slippery due to the rains and the streams become difficult to cross. Hence it is best to avoid making a trip to the fort at such times.
</p>

<h2 class="card-title">How to reach Ballalarayana Durga:</h2>
<p class="card-text">The place is well connected through road network from Bangalore. There are frequent buses available from Bangalore to Chikmagalur.
There is also a railway station in Chikmagalur that operates a daily passenger train. However, the town of Chikmagalur has no airport. The nearest airport is the one that is located in Mangalore.
To reach the Ballalarayana Durga fort one can avail the KSRTC buses that operate from Dharmasthala to Mundaje or Sunkasaale. Usually the trek to the fort starts from Mundaje or Sunkasaale.
The trek is a difficult one but the natural scenery it presents makes the effort to visit the fort worthwhile.
</p>

